    private void monthBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_monthBoxActionPerformed
       
         int yr = Integer.valueOf(yearBox.getText());
        String mn = monthBox.getSelectedItem().toString();

        String contents[][] = new String[30][30];

        try
        {
            Class.forName(driver);
        }
        catch (ClassNotFoundException e)
        {
            e.printStackTrace();
        }

        try {
            String sql = "SELECT amount,id,itemCode,staffID FROM expenses WHERE Year="+yr+" AND month='"+mn+"'";
            connection = DriverManager.getConnection(url,user,password);
            preparedStatement=connection.prepareStatement(sql);
            resultSet = preparedStatement.executeQuery();

            int i=0;
            while(resultSet.next())
            {
                contents[i][0]=String.valueOf(resultSet.getInt("id"));
                contents[i][1]=String.valueOf(resultSet.getDouble("amount"));
                contents[i][2]=String.valueOf(resultSet.getInt("itemCode"));
                contents[i][3]=String.valueOf(resultSet.getInt("staffID"));
                i = i + 1;
            }

            String sql2 = "SELECT SUM(amount) FROM expenses WHERE Year="+yr+" AND month='"+mn+"'";
            connection = DriverManager.getConnection(url,user,password);
            preparedStatement=connection.prepareStatement(sql2);
            resultSet = preparedStatement.executeQuery();
            while(resultSet.next())
            {

                amount.setText(resultSet.getString("SUM(amount)"));

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            contents,
            new String [] {
                "ID", "Amount" , "Item Code" , "Staff ID"
            }
        ) {
            boolean[] canEdit = new boolean [] {
                false,false,false,false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });

    }//GEN-LAST:event_monthBoxActionPerformed

